package main

// В данном примере главная горутина отправляет данные в буферизованный канал с емкостью 1.
// После того как канал заполнился, она пытается отправить ещё данные, что приводит к deadlock.
func main() {
	ch := make(chan int, 1) // Буферизованный канал с емкостью 1

	println("Главная горутина: Отправка данных в канал...")
	ch <- 42 // Отправка данных в буферизованный канал

	println("Главная горутина: Попытка отправить больше данных в канал...")
	ch <- 100 // Попытка отправить больше данных в полный канал

	println("Главная горутина: Завершено")
}
